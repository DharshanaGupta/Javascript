function changeStyle()
{
   var para= document.getElementsByTagName("p");
   para[0].style.color="blue";
   para[1].style.fontSize=26;
   para[2].style.fontWeight="bold";
   para[3].style.fontStyle="italic";
   para[4].style.backgroundColor="grey";
   //we can also use for loop to iterate for changing the same style
}